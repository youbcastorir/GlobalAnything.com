// Language detection patterns
const LANG_DETECT_PATTERNS = [
  { lang: "ar", name: "Arabic",  pattern: /[\u0600-\u06FF\u0750-\u077F]/ },
  { lang: "fa", name: "Persian", pattern: /[\u0600-\u06FF][\u067E\u0686\u06AF\u06CC]/ },
  { lang: "ru", name: "Russian", pattern: /[\u0400-\u04FF]/ },
  { lang: "zh", name: "Chinese", pattern: /[\u4E00-\u9FFF]/ },
  { lang: "ja", name: "Japanese",pattern: /[\u3040-\u309F\u30A0-\u30FF]/ },
  { lang: "ko", name: "Korean",  pattern: /[\uAC00-\uD7AF\u1100-\u11FF]/ },
  { lang: "hi", name: "Hindi",   pattern: /[\u0900-\u097F]/ },
  { lang: "bn", name: "Bengali", pattern: /[\u0980-\u09FF]/ },
  { lang: "fr", name: "French",  pattern: /\b(le|la|les|de|du|des|un|une|est|sont|dans|pour|avec|sur|par)\b/i },
  { lang: "es", name: "Spanish", pattern: /\b(el|la|los|las|de|del|en|por|con|una?|este|esta|que|para)\b/i },
  { lang: "de", name: "German",  pattern: /\b(der|die|das|ein|eine|und|oder|aber|nicht|ist|sind)\b/i },
  { lang: "en", name: "English", pattern: /\b(the|is|are|was|were|have|has|this|that|with|from|they|been)\b/i }
];

function detectLanguage(text) {
  if (!text || text.trim().length < 2) return null;
  for (const { lang, name, pattern } of LANG_DETECT_PATTERNS) {
    if (pattern.test(text)) return { code: lang, name };
  }
  return { code: "en", name: "English" };
}
